#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char		ch;
	const char	*sc = NULL;

	ch = c;
	while (*s != '\0')
	{
		if (*s == ch)
			sc = s;
		s++;
	}
	if (*s == '\0' && c == '\0')
		sc = s;
	return ((char *)sc);
}
